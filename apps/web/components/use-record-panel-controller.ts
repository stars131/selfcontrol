"use client";

import type { ControllerProps } from "./record-panel-controller.types";
import { createRecordPanelControllerMediaHandlers } from "./record-panel-controller-media-handlers";
import { createRecordPanelControllerRecordHandlers } from "./record-panel-controller-record-handlers";
import { useRecordPanelControllerSync } from "./use-record-panel-controller-sync";
import { useRecordPanelControllerState } from "./use-record-panel-controller-state";
import { useRecordPanelControllerViewData } from "./use-record-panel-controller-view-data";

export function useRecordPanelController({
  authToken,
  workspaceId,
  records,
  selectedRecordId,
  mediaAssets,
  mediaDeadLetterOverview,
  onSaveRecord,
  onCreateReminder,
  onDeleteMedia,
  onDeleteRecord,
  onBulkRetryMediaDeadLetter,
  onRefreshMediaStatus,
  onApplyRecordFilter,
  onCreateSearchPreset,
  onDeleteSearchPreset,
  onRetryMedia,
  onUploadMedia,
  recordFilter,
}: ControllerProps) {
  const {
    locale,
    avoidCount,
    foodCount,
    selectedRecord,
    selectedLocationReview,
    selectedLocationHistory,
    selectedRecordMediaSizeLabel,
    actionableDeadLetterIds,
    mediaIssueCopy,
    panelCopy,
    detailCopy,
    formatAvoidCountLabel,
    formatFileCountLabel,
    formatHistoryTimestampLabel,
    formatRecordTimestampLabel,
    formatReminderEnabledLabel,
    formatReminderStatusLabel,
    formatReminderTimestampLabel,
    formatReviewStatusLabel,
    formatTimelineCountLabel,
    formatTimelineDateLabel,
    summarizeHistoryActionLabel,
    summarizeRecordFilterLabel,
  } = useRecordPanelControllerViewData({
    mediaAssets,
    mediaDeadLetterOverview,
    records,
    selectedRecordId,
  });
  const {
    form,
    setForm,
    saving,
    setSaving,
    deleting,
    setDeleting,
    uploading,
    setUploading,
    refreshingMediaId,
    setRefreshingMediaId,
    retryingMediaId,
    setRetryingMediaId,
    bulkRetryingDeadLetter,
    setBulkRetryingDeadLetter,
    downloadingMediaId,
    setDownloadingMediaId,
    deletingMediaId,
    setDeletingMediaId,
    reminderForm,
    setReminderForm,
    savingReminder,
    setSavingReminder,
    locationReviewForm,
    setLocationReviewForm,
    viewMode,
    setViewMode,
    filterDraft,
    setFilterDraft,
    presetName,
    setPresetName,
    selectedDeadLetterIds,
    setSelectedDeadLetterIds,
    error,
    setError,
  } = useRecordPanelControllerState(recordFilter);
  useRecordPanelControllerSync({
    actionableDeadLetterIds,
    recordFilter,
    selectedRecord,
    setFilterDraft,
    setForm,
    setLocationReviewForm,
    setReminderForm,
    setSelectedDeadLetterIds,
  });
  const recordHandlers = createRecordPanelControllerRecordHandlers({
    detailCopy,
    filterDraft,
    form,
    locationReviewForm,
    onApplyRecordFilter,
    onCreateReminder,
    onCreateSearchPreset,
    onDeleteRecord,
    onDeleteSearchPreset,
    onSaveRecord,
    presetName,
    reminderForm,
    selectedRecord,
    setDeleting,
    setError,
    setForm,
    setPresetName,
    setReminderForm,
    setSaving,
    setSavingReminder,
  });
  const mediaHandlers = createRecordPanelControllerMediaHandlers({
    authToken,
    detailCopy,
    mediaDeadLetterOverview,
    onBulkRetryMediaDeadLetter,
    onDeleteMedia,
    onRefreshMediaStatus,
    onRetryMedia,
    onUploadMedia,
    selectedDeadLetterIds,
    selectedRecord,
    setBulkRetryingDeadLetter,
    setDeletingMediaId,
    setDownloadingMediaId,
    setError,
    setRefreshingMediaId,
    setRetryingMediaId,
    setSelectedDeadLetterIds,
    setUploading,
    workspaceId,
  });
  return {
    locale,
    avoidCount,
    foodCount,
    selectedRecord,
    form,
    setForm,
    saving,
    deleting,
    uploading,
    refreshingMediaId,
    retryingMediaId,
    bulkRetryingDeadLetter,
    downloadingMediaId,
    deletingMediaId,
    reminderForm,
    setReminderForm,
    savingReminder,
    locationReviewForm,
    setLocationReviewForm,
    viewMode,
    setViewMode,
    filterDraft,
    setFilterDraft,
    presetName,
    setPresetName,
    selectedDeadLetterIds,
    error,
    selectedLocationReview,
    selectedLocationHistory,
    selectedRecordMediaSizeLabel,
    mediaIssueCopy,
    panelCopy,
    detailCopy,
    formatAvoidCountLabel,
    formatFileCountLabel,
    formatHistoryTimestampLabel,
    formatRecordTimestampLabel,
    formatReminderEnabledLabel,
    formatReminderStatusLabel,
    formatReminderTimestampLabel,
    formatReviewStatusLabel,
    formatTimelineCountLabel,
    formatTimelineDateLabel,
    summarizeHistoryActionLabel,
    summarizeRecordFilterLabel,
    ...recordHandlers,
    ...mediaHandlers,
  };
}
